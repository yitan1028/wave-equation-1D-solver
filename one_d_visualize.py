def plot_wave_snapshots(result: SolverResult, output_dir: str | Path, snapshot_steps: Sequence[int] | None = None) -> Path:
    output_dir = ensure_output_dir(output_dir)
    path = output_dir / "03_wavefield_snapshots.png"

    if snapshot_steps is None:
        snapshot_steps = [110, 210, 310, 430]

    ymax = np.percentile(np.abs(result.wavefield_history), 99.8)
    if ymax == 0:
        ymax = 1.0

    fig, axes = plt.subplots(len(snapshot_steps), 1, figsize=(11, 8.2), sharex=True)
    if len(snapshot_steps) == 1:
        axes = [axes]

    receiver_subset = np.linspace(0, len(result.receiver_xs) - 1, 5, dtype=int)

    for ax, step in zip(axes, snapshot_steps):
        step = int(np.clip(step, 0, len(result.t) - 1))
        ax.plot(result.x, result.wavefield_history[step], linewidth=1.8)
        ax.axvline(result.x[result.source_index], linestyle="--", label="Source")
        ax.axvline(result.x[result.calculation_index], linestyle="-.", label="Selected calculation point")
        for k, r in enumerate(receiver_subset):
            ax.axvline(result.receiver_xs[r], linestyle=":", alpha=0.45, label="Receivers" if k == 0 else None)
        ax.set_ylim(-1.15 * ymax, 1.15 * ymax)
        ax.set_title(f"Wavefield u(x,t) at t = {result.t[step]:.3f} s")
        ax.set_ylabel("Amplitude")
        ax.grid(True, alpha=0.3)

    axes[-1].set_xlabel("Position x (m)")
    axes[0].legend(loc="best")
    fig.tight_layout()
    fig.savefig(path, dpi=200)
    plt.close(fig)
    return path


def plot_receiver_traces(result: SolverResult, output_dir: str | Path) -> Path:
    output_dir = ensure_output_dir(output_dir)
    path = output_dir / "04_receiver_traces.png"

    selected = np.linspace(0, result.receiver_data.shape[1] - 1, 5, dtype=int)
    data = result.receiver_data[:, selected]
    max_abs = np.max(np.abs(data))
    if max_abs == 0:
        max_abs = 1.0
    offset = 1.7 * max_abs

    fig, ax = plt.subplots(figsize=(10.5, 5))
    for k, r in enumerate(selected):
        ax.plot(result.t, result.receiver_data[:, r] + k * offset, linewidth=1.5, label=f"x={result.receiver_xs[r]:.0f} m")

    ax.set_title("Selected receiver traces generated by the forward solver", fontsize=16)
    ax.set_xlabel("Time (s)")
    ax.set_ylabel("Amplitude with vertical offset")
    ax.grid(True, alpha=0.3)
    ax.legend(loc="best", title="Receiver")
    fig.tight_layout()
    fig.savefig(path, dpi=200)
    plt.close(fig)
    return path


def plot_seismic_record(result: SolverResult, output_dir: str | Path) -> Path:
    output_dir = ensure_output_dir(output_dir)
    path = output_dir / "05_seismic_record_time_receiver.png"

    vmax = np.percentile(np.abs(result.receiver_data), 99.5)
    if vmax == 0:
        vmax = 1.0

    extent = [result.receiver_xs[0], result.receiver_xs[-1], result.t[-1], result.t[0]]

    fig, ax = plt.subplots(figsize=(9.5, 6.2))
    im = ax.imshow(result.receiver_data, aspect="auto", extent=extent, vmin=-vmax, vmax=vmax, cmap="seismic")
    ax.axvline(result.x[result.source_index], linestyle="--", color="black", alpha=0.7, label="Source")
    ax.axvline(result.x[result.calculation_index], linestyle="-.", color="black", alpha=0.7, label="Selected calculation point")

    ax.set_title("Forward-generated seismic record: time × receiver position", fontsize=16)
    ax.set_xlabel("Receiver position x (m)")
    ax.set_ylabel("Time (s)")
    ax.legend(loc="upper right")
    cbar = fig.colorbar(im, ax=ax)
    cbar.set_label("Recorded amplitude")
    fig.tight_layout()
    fig.savefig(path, dpi=200)
    plt.close(fig)
    return path


