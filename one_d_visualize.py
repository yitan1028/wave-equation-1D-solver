"""
one_d_visualize.py

Visualization for the V3 teaching-oriented 1D wave solver.

Important V3 outputs:
- 01_velocity_model.png shows a clearly variable 1D velocity profile.
- 05_seismic_record_time_receiver.png uses many receivers and receiver position on x-axis.
- 07_numerical_update_examples.png prints the finite-difference formula and actual numeric substitutions.
"""

from __future__ import annotations

from pathlib import Path
from typing import Sequence

import numpy as np
import matplotlib.pyplot as plt
from matplotlib.animation import FuncAnimation, PillowWriter

from one_d_solver import SolverResult


def ensure_output_dir(output_dir: str | Path) -> Path:
    path = Path(output_dir)
    path.mkdir(parents=True, exist_ok=True)
    return path


def plot_velocity_model(result: SolverResult, output_dir: str | Path) -> Path:
    output_dir = ensure_output_dir(output_dir)
    path = output_dir / "01_velocity_model.png"

    fig, ax = plt.subplots(figsize=(10.5, 4.3))
    ax.plot(result.x, result.velocity, linewidth=2.4, label="Velocity v(x)")
    ax.axvline(result.x[result.source_index], linestyle="--", linewidth=2.0, label="Source")
    ax.axvline(result.x[result.calculation_index], linestyle="-.", linewidth=2.0, label="Selected calculation point")

    subset = np.linspace(0, len(result.receiver_xs) - 1, 7, dtype=int)
    for k, r in enumerate(subset):
        ax.axvline(result.receiver_xs[r], linestyle=":", alpha=0.50, label="Receivers" if k == 0 else None)

    ax.set_title("1D variable velocity model", fontsize=16)
    ax.set_xlabel("Position x (m)")
    ax.set_ylabel("Velocity v(x) (m/s)")
    ax.grid(True, alpha=0.3)
    ax.legend(loc="best")
    fig.tight_layout()
    fig.savefig(path, dpi=200)
    plt.close(fig)
    return path


